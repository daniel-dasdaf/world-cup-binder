const STORAGE_KEY = "panini_2026_binder";

function loadState() {
  return JSON.parse(localStorage.getItem(STORAGE_KEY)) || {};
}

function saveState(state) {
  localStorage.setItem(STORAGE_KEY, JSON.stringify(state));
}

function applyState(binder, state) {
  // FWC
  binder.fwc.forEach(item => {
    if (state[item.id] !== undefined) {
      item.owned = state[item.id];
    }
  });

  // Groups
  Object.values(binder.groups).forEach(teams => {
    teams.forEach(team => {
      team.stickers.forEach(sticker => {
        const key = `${team.code}_${sticker.localId}`;
        if (state[key] !== undefined) {
          sticker.owned = state[key];
        }
      });
    });
  });
}

function buildState(binder) {
  const state = {};

  binder.fwc.forEach(item => {
    state[item.id] = item.owned;
  });

  Object.values(binder.groups).forEach(teams => {
    teams.forEach(team => {
      team.stickers.forEach(sticker => {
        const key = `${team.code}_${sticker.localId}`;
        state[key] = sticker.owned;
      });
    });
  });

  return state;
}

function getGroupProgress(teams) {
  let total = 0;
  let owned = 0;

  teams.forEach(team => {
    total += team.stickers.length;
    owned += team.stickers.filter(s => s.owned).length;
  });

  return { owned, total };
}

function getTeamProgress(team) {
  const total = team.stickers.length;
  const owned = team.stickers.filter(s => s.owned).length;

  return { owned, total };
}

function createCard(text, owned, onClick = () => {}) {
  const div = document.createElement("div");
   div.className = `
    cursor-pointer border rounded-md p-2 text-sm font-bold hover:border-[black]
    ${owned ? "opacity-100 border-[#2f6e58] text-[#2f6e58]" : "opacity-40"}
  `;
  div.textContent = text;


  div.addEventListener("click", onClick);
  return div;
}

function renderFWC() {
  const section = document.createElement("div");
  section.className = "fwc p-[10px]";

  const title = document.createElement("h2");
  title.textContent = "FWC";
  title.className = "font-bold text-[24px] mb-[10px]";
  section.appendChild(title);

  const row = document.createElement("div");
  row.className = "stickers flex flex-row flex-wrap gap-[10px]";

  binder.fwc.forEach(item => {
    const card = createCard(
      `${item.id} - ${item.name}`,
      item.owned,
      () => {
        // 🔥 toggle state
        item.owned = !item.owned;

        // 💾 save
        const state = buildState(binder);
        saveState(state);


        card.classList.toggle("opacity-40");
        card.classList.toggle("opacity-100");
        card.classList.toggle("text-[#2f6e58]");
        card.classList.toggle("border-[#2f6e58]");
      }
    );

    row.appendChild(card);
  });

  section.appendChild(row);
  app.appendChild(section);
}

